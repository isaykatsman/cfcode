r = c1t[2]
R = c2t[2]
d = ((c1t[0]-c2t[0])**2 + (c1t[1]-c2t[1])**2).sqrt()

print(d)

if d == 0:
    ai = Decimal(pi)*(min(r,R)**2)
elif r+R <= d:
    ai = 0
else:
    ai1 = (r**2)*acdec(((d**2)+(r**2)-(R**2))/(2*d*r))
    ai2 = (R**2)*acdec(((d**2)+(R**2)-(r**2))/(2*d*R))
    ai3 = Decimal(1/2)*((r-d+R)*(d+r-R)*(d-r+R)*(d+r+R)).sqrt()
    ai = ai1 + ai2 - ai3

print(ai)
